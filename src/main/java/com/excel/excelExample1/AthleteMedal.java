package com.excel.excelExample1;

public class AthleteMedal {
	
	private int gold;
	private int silver;
	private int bronze;
	
	
	public AthleteMedal() {
		this.gold = 0;
		this.silver = 0;
		this.bronze = 0;
	}
	
	public int getGold() {
		return gold;
	}
	public void setGold(int gold) {
		this.gold = gold;
	}
	public int getSilver() {
		return silver;
	}
	public void setSilver(int silver) {
		this.silver = silver;
	}
	public int getBronze() {
		return bronze;
	}
	public void setBronze(int bronze) {
		this.bronze = bronze;
	}


	
}
